package net.loyin.netService;

/**
 * Created by Chao on 2015/11/10.
 */
public class BusinessProcess {
    /**
     * 报文处理类
     */
    private String ProcessClass;
    /**
     * 报文版本
     */
    private String PacketsVersion;
    /**
     * 请求代码
     */
    private String ProcessCode;
    /**
     * 业务处理ID
     */
    private String ProcessId;

    public String getProcessClass() {
        return ProcessClass;
    }

    public void setProcessClass(String processClass) {
        ProcessClass = processClass;
    }

    public String getPacketsVersion() {
        return PacketsVersion;
    }

    public void setPacketsVersion(String packetsVersion) {
        PacketsVersion = packetsVersion;
    }

    public String getProcessCode() {
        return ProcessCode;
    }

    public void setProcessCode(String processCode) {
        ProcessCode = processCode;
    }

    public String getProcessId() {
        return ProcessId;
    }

    public void setProcessId(String processId) {
        ProcessId = processId;
    }

}
